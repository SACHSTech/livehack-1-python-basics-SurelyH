# Input celsius 
celsius = float(input("Please input the temperature in celsius: "))

# Calculations
fahrenheit = (9 /5 *celsius +  32)

# Output in fahrenheit 
print("The temperature would be " + str(fahrenheit) + " degrees fahrenheit.")